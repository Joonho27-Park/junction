// 색상 상수 정의
// RGB 값을 ImGui의 32비트 정수 색상 형식으로 변환
// 형식: R + (G << 8) + (B << 16) + (A << 24)

/// 열차 미점유 (255, 255, 255). 백색
pub const TRAIN_UNOCCUPIED: u32 = 255 + (255 << 8) + (255 << 16) + (255 << 24);

/// 열차 점유 또는 장애 (255, 0, 0). 적색
pub const TRAIN_OCCUPIED_OR_FAULT: u32 = 255 + (0 << 8) + (0 << 16) + (255 << 24);

/// 진로 요구 중 (255, 255, 0). 적색황색
pub const ROUTE_REQUESTING: u32 = 255 + (255 << 8) + (0 << 16) + (255 << 24);

/// 진로 설정 (0, 255, 0). 녹색
pub const ROUTE_SET: u32 = 0 + (255 << 8) + (0 << 16) + (255 << 24);

/// 글자색 (0, 0, 255). 청색
pub const TEXT_COLOR: u32 = 0 + (0 << 8) + (255 << 16) + (255 << 24);

// 기존 UI에서 사용되는 색상들도 추가
/// 캔버스 배경색 (60, 60, 60)
pub const CANVAS_BG: u32 = 60 + (60 << 8) + (60 << 16) + (255 << 24);

/// 선 색상 (208, 208, 175)
pub const LINE_COL: u32 = 208 + (208 << 8) + (175 << 16) + (255 << 24);

/// TVD 색상 (175, 255, 175)
pub const TVD_COL: u32 = 175 + (255 << 8) + (175 << 16) + (255 << 24);

/// 선택된 요소 색상 (175, 175, 255)
pub const SELECTED_COL: u32 = 175 + (175 << 8) + (255 << 16) + (255 << 24);

/// 백색 (255, 255, 255)
pub const WHITE: u32 = 255 + (255 << 8) + (255 << 16) + (255 << 24);

/// 호버 색상 (255, 50, 50)
pub const LINE_HOVER_COL: u32 = 255 + (50 << 8) + (50 << 16) + (255 << 24);

/// 점유된 구간 색상 (255, 65, 55)
pub const OCCUPIED_COL: u32 = 255 + (65 << 8) + (55 << 16) + (255 << 24);

/// 예약된 구간 색상 (55, 255, 55)
pub const RESERVED_COL: u32 = 55 + (255 << 8) + (55 << 16) + (255 << 24);

/// 겹침 구간 색상 (209, 208, 22)
pub const OVERLAP_COL: u32 = 209 + (208 << 8) + (22 << 16) + (255 << 24); 